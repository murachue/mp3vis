export function sortedhufftab(rtab: ReadonlyHufftab) {
    const tab = [...rtab];
    tab.sort((a, b) => {
        const z = a[0].reduce((r, x, i) => {
            // const z = r ? r : (b[0][i] - x); // 1 to 0, shorter to longer
            const z = r ? r : (x - b[0][i]); // 0 to 1, easier to expand
            // console.dir([i, a, b, z]);
            return z;
        }, 0);
        // console.dir(z);
        return z;
    });
    return tab;
}
// type CompactHufftab = [number, BiVal][] | [number, QuadVal][];
type LengthyHufftab = [number, readonly number[]][];
type CompactHufftab = [number, (readonly number[])[]][];
export function compactify(tab: ReadonlyHufftab) {
    const tab2: LengthyHufftab = tab.map(([huff, val]) => [huff.length, val]);

    const tab3 = tab2.reduce((pre, cur) => {
        const last = pre[pre.length - 1];
        if (last?.[0] === cur[0]) {
            last[1].push(cur[1]);
        } else {
            pre.push([cur[0], [cur[1]]]);
        }
        return pre;
    }, [] as CompactHufftab);

    return tab3;
}
export function huffincr(huff: number[]) {
    for (let i = huff.length - 1; 0 <= i; i--) {
        if (huff[i] === 0) {
            huff[i] = 1;
            return;
        } else {
            huff[i] = 0;
        }
    }
}
// type RestructuredHufftab =
//     [number[], BiVal][] |
//     [number[], QuadVal][];
type RestructuredHufftab = [number[], readonly number[]][];
export function expandify(tab: CompactHufftab) {
    const extab: RestructuredHufftab = [];
    const huff: number[] = [];

    for (const [len, ents] of tab) {
        const ndel = Math.max(0, huff.length - len);
        const nadd = Math.max(0, len - huff.length);
        huff.splice(len, ndel, ...Array(nadd).fill(0));

        for (const ent of ents) {
            extab.push([[...huff], ent]);
            huffincr(huff);
        }
    }

    return extab;
}
// type Huffvalue = readonly [number, number] | readonly [number, number, number, number];
// type Hufftree = [Hufftree, Hufftree] | Huffvalue;
// type PartialHufftree = [PartialHufftree | null, PartialHufftree | null] | Huffvalue;
export function treeify(tab: ReadonlyHufftab) {
    const tree: /* PartialHufftree */any[] = [null, null];

    for (const [huff, val] of tab) {
        let cur = tree;
        for (const side of huff.slice(0, -1)) {
            // if (typeof cur[0] === "number") { throw new Error("bad"); }
            cur = (cur[side] || (cur[side] = [null, null])) as any;
        }
        cur[huff[huff.length - 1]] = val;
    }

    return tree as /* Hufftree */any[];
}

